import { test } from '../index.ts';

Deno.test('should validate taxID', () => {
  test({
    validator: 'isTaxID',
    valid: [
      '01-1234567',
      '01 1234567',
      '011234567',
      '10-1234567',
      '02-1234567',
      '67-1234567',
      '15-1234567',
      '31-1234567',
      '99-1234567',
    ],
    invalid: [
      '0-11234567',
      '01#1234567',
      '01  1234567',
      '01 1234 567',
      '07-1234567',
      '28-1234567',
      '96-1234567',
    ],
  });

  test({
    validator: 'isTaxID',
    args: ['is-NOT'],
    error: [
      '01-1234567',
      '01 1234567',
      '011234567',
      '0-11234567',
      '01#1234567',
      '01  1234567',
      '01 1234 567',
      '07-1234567',
      '28-1234567',
      '96-1234567',
    ],
  });
});
